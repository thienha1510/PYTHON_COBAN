Day = int(input("Nhap ngay: "))

if Day == 2:
    print("Hôm nay là Thứ Hai")
elif Day == 3:
    print("Hôm nay là Thứ Ba")
elif Day == 4:
    print("Hôm nay là Thứ Tư")
elif Day == 5:
    print("Hôm nay là Thứ Năm")
elif Day == 6:
    print("Hôm nay là Thứ Sáu")
elif Day == 7:
    print("Hôm nay là Thứ Tảy")
else:
    print("Hôm nay là Chủ Nhật")